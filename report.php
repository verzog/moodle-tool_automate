/**
 * View reports generated by automation rules on screen, with a download
 * link. With no parameter it lists every saved report; with ?file=NAME
 * it renders that one report as a table.
 *
 * @package    tool_automate
 * @copyright  2026 verzog <verzog@gmail.com>
 * @license    https://www.gnu.org/copyleft/gpl.html GNU GPL v3 or later
