require(__DIR__ . '/../../../config.php');
require_once($CFG->libdir . '/adminlib.php');

admin_externalpage_setup('tool_automate');
$context = context_system::instance();
require_capability('tool/automate:manage', $context);

$file = optional_param('file', '', PARAM_FILE);

$baseurl = new moodle_url('/admin/tool/automate/report.php');
$PAGE->set_url($baseurl, $file !== '' ? ['file' => $file] : []);
$PAGE->set_title(get_string('savedreports', 'tool_automate'));
$PAGE->set_heading(get_string('savedreports', 'tool_automate'));
$PAGE->add_body_class('tool_automate-page');

$fs = get_file_storage();

echo $OUTPUT->header();
echo html_writer::link(
    new moodle_url('/admin/tool/automate/index.php'),
    get_string('back', 'tool_automate'),
    ['class' => 'tool_automate_back']
);

if ($file !== '') {
    // Render a single report. PARAM_FILE plus the fixed
    // component/area/itemid/path means there's no path-traversal surface.
    $stored = $fs->get_file($context->id, 'tool_automate', 'reports', 0, '/', $file);
    if (!$stored || $stored->is_directory()) {
        echo $OUTPUT->notification(get_string('reportgone', 'tool_automate'), 'error');
        echo $OUTPUT->footer();
        exit;
    }

    echo $OUTPUT->heading(s($file), 3);

    $downloadurl = moodle_url::make_pluginfile_url(
        $context->id,
        'tool_automate',
        'reports',
        0,
        '/',
        $file,
        true
    );
    echo $OUTPUT->single_button(
        $downloadurl,
        get_string('downloadreport', 'tool_automate'),
        'get'
    );

    $content = $stored->get_content();
    if (substr(strtolower($file), -4) === '.csv') {
        // Parse the CSV through a stream so quoted fields with embedded
        // commas or newlines are handled correctly.
        $rows = [];
        $fh = fopen('php://temp', 'r+');
        fwrite($fh, $content);
        rewind($fh);
        while (($row = fgetcsv($fh)) !== false) {
            $rows[] = $row;
        }
        fclose($fh);

        if ($rows) {
            $table = new html_table();
            $table->attributes['class'] = 'generaltable tool_automate_report';
            $table->head = array_shift($rows);
            foreach ($rows as $row) {
                $table->data[] = array_map('s', $row);
            }
            echo html_writer::table($table);
        } else {
            echo $OUTPUT->notification(get_string('reportempty', 'tool_automate'), 'info');
        }
    } else {
        echo html_writer::tag('pre', s($content), ['class' => 'tool_automate_report_text']);
    }

    echo $OUTPUT->footer();
    exit;
}

// No file requested: list every saved report, newest first.
$files = $fs->get_area_files($context->id, 'tool_automate', 'reports', 0, 'timecreated DESC', false);
if (!$files) {
    echo $OUTPUT->notification(get_string('noreports', 'tool_automate'), 'info');
    echo $OUTPUT->footer();
    exit;
}

$table = new html_table();
$table->head = [
    get_string('reportfile', 'tool_automate'),
    get_string('when', 'tool_automate'),
    get_string('reportsize', 'tool_automate'),
    get_string('actions', 'tool_automate'),
];
foreach ($files as $stored) {
    $name = $stored->get_filename();
    $viewurl = new moodle_url($baseurl, ['file' => $name]);
    $downloadurl = moodle_url::make_pluginfile_url(
        $context->id,
        'tool_automate',
        'reports',
        0,
        '/',
        $name,
        true
    );
    $links = html_writer::link($viewurl, get_string('viewreport', 'tool_automate'))
        . ' | '
        . html_writer::link($downloadurl, get_string('downloadreport', 'tool_automate'));
    $table->data[] = [
        html_writer::link(new moodle_url($baseurl, ['file' => $name]), s($name)),
        userdate($stored->get_timecreated()),
        display_size($stored->get_filesize()),
        $links,
    ];
}
echo html_writer::table($table);

echo $OUTPUT->footer();
